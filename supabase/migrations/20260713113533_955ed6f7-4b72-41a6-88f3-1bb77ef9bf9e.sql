Conserver une bouteille ouverte n'est pas un détail secondaire. En restauration, la perte naît souvent de petites décisions répétées : une bouteille ouverte "au cas où", un blanc laissé hors du froid, un rouge servi trois jours après ouverture, un effervescent sans pression suffisante ou un verre premium proposé sans système de conservation. Chaque cas abîme la marge et la confiance.

Apprendre le vin en salle signifie aussi savoir quand un vin est encore vivant et quand il ne doit plus être servi. Il n'existe pas de règle unique. Un blanc aromatique, un rouge léger, un rouge structuré, un vin fortifié et un effervescent n'évoluent pas de la même manière après ouverture. L'équipe a besoin d'un protocole simple : noter l'ouverture, protéger de l'oxygène, gérer la température, goûter avant le service et décider si la bouteille se vend, sert à la formation, part en cuisine ou doit être écartée.

**Résumé IA :** conserver une bouteille de vin ouverte au restaurant demande de maîtriser oxygène, température, temps, style de vin et système de préservation. L'objectif est de réduire la perte sans dégrader l'expérience client, surtout pour le vin au verre et les références premium ou lentes.

## Ce qui se passe après ouverture

À l'ouverture, l'oxygène entre dans la bouteille. Un peu d'oxygène peut aider certains vins; trop d'oxygène éteint le fruit, aplatit les arômes, durcit l'amertume et mène à l'oxydation. La vraie question n'est pas seulement "combien de jours le vin tient-il ?". C'est : "dans ces conditions, ce vin représente-t-il encore ce que nous promettons au client ?".

En salle, distinguez trois phases. Première : ouverture, le vin est servi comme prévu. Deuxième : fenêtre utile, le vin reste net, expressif et vendable, même s'il change légèrement. Troisième : risque, la bouteille n'est pas forcément défectueuse mais elle ne donne plus l'expérience liée au prix. Le protocole évite de confondre les deux dernières phases.

## Repères par style

Les durées varient selon conservation, niveau, température et style, mais ces repères aident :

- blancs frais et aromatiques : souvent 1 à 2 jours au froid avec fermeture correcte;
- blancs plus amples ou élevés : 2 à 3 jours s'ils restent froids et sont goûtés;
- rosés : 1 à 2 jours, surtout si le fruit frais est central;
- rouges légers : 1 à 2 jours, plutôt au frais;
- rouges structurés : 2 à 4 jours avec bonne conservation;
- effervescents : le même service ou 24 heures avec bouchon à pression fiable;
- vins fortifiés : souvent plus longs, mais toujours contrôlés.

Ces repères n'autorisent pas un service automatique. Ce sont des alertes de vérification. Dès qu'une bouteille atteint sa fenêtre, elle doit être goûtée.

## Oxygène, température et niveau

Trois variables expliquent la majorité des problèmes. La première est l'oxygène : plus il reste d'air dans la bouteille, plus le vin évolue vite. Une bouteille au tiers change bien plus vite qu'une bouteille presque pleine. La deuxième est la température : la chaleur accélère le vieillissement. Beaucoup de rouges ouverts se gardent mieux au froid puis se remettent à température de service. La troisième est la manipulation : laisser ouvert, déplacer sans cesse ou exposer à la lumière réduit la durée utile.

La règle de salle peut rester simple : fermer toujours, dater toujours, mettre au froid quand le style le permet et goûter avant de revendre. Si personne ne sait quand une bouteille a été ouverte, elle est déjà un risque.

## Systèmes de préservation

Tous les restaurants n'ont pas besoin du même matériel. Un bouchon simple aide peu, mais mieux que rien. La pompe à vide peut convenir aux vins qui tournent vite. Le gaz inerte réduit le contact avec l'oxygène. Les systèmes à aiguille ou avancés permettent de vendre des verres premium sans ouvrir totalement la bouteille.

La décision doit être économique et opérationnelle. Si un verre d'entrée de gamme tourne dans le même service, froid et fermeture peuvent suffire. Si un verre premium se vend lentement, l'ouverture sans préservation transforme une marge théorique en perte réelle. Avant d'acheter un système, regardez les données : nombre de références au verre, prix moyen, verres réellement servis par bouteille, jours ouverts et vin jeté.

## Protocole quotidien pour le vin au verre

Un protocole utile tient en cinq étapes :

1. Noter date et heure d'ouverture.
2. Indiquer le service ou le responsable.
3. Conserver selon le style : froid, bouchon, gaz ou système dédié.
4. Goûter avant le premier service du lendemain.
5. Décider : vendre, former, retirer, cuisine ou perte.

La quatrième étape est essentielle. Le client ne doit pas découvrir qu'un verre est fatigué. Une petite dégustation en début de service protège la table et forme l'équipe.

## Décider si la bouteille reste vendable

Posez trois questions :

- Le nez est-il encore net, reconnaissable et agréable ?
- La bouche garde-t-elle fraîcheur, équilibre et finale ?
- Le verre vaut-il encore le prix affiché sur la carte ?

Si la troisième réponse est incertaine, ne le vendez pas comme verre normal. Il peut servir à la formation, à la cuisine si le restaurant l'autorise ou à montrer l'effet de l'oxygène. Mais la perte ne doit pas être transférée au client.

## Erreurs fréquentes

La première erreur est d'ouvrir trop de références au verre sans estimer la rotation. La deuxième est de croire que le froid corrige tout. La troisième est de traiter les styles fragiles et résistants de la même façon. La quatrième est de ne pas noter les ouvertures. La cinquième est de garder des verres premium sans le système nécessaire.

Il existe aussi une erreur culturelle : craindre de retirer une bouteille. Retirer à temps n'est pas seulement perdre de l'argent; c'est souvent éviter une plainte, protéger la réputation et comprendre quelles références ne doivent pas être vendues au verre.

