## Was nach dem Öffnen passiert

Beim Öffnen kommt Sauerstoff in die Flasche. Ein wenig Sauerstoff kann manchen Weinen helfen; zu viel Sauerstoff nimmt Frucht, macht Aromen flach, verstärkt Bitterkeit und führt zu Oxidation. Die operative Frage lautet nicht nur "wie viele Tage hält der Wein?". Sie lautet: "repräsentiert dieser Wein unter diesen Bedingungen noch das, was wir dem Gast versprechen?".

Im Service helfen drei Phasen. Erstens: Öffnung, der Wein wird wie geplant serviert. Zweitens: nutzbares Fenster, der Wein bleibt sauber, ausdrucksstark und verkaufbar, auch wenn er sich leicht verändert. Drittens: Risiko, die Flasche ist nicht zwingend fehlerhaft, bietet aber nicht mehr die Erfahrung zum Preis. Ein gutes Protokoll verhindert, dass Phase zwei und drei verwechselt werden.

## Orientierung nach Stil

Die Dauer hängt von Lagerung, Füllstand, Temperatur und Stil ab, aber diese Werte helfen:

- frische aromatische Weissweine: meist 1 bis 2 Tage mit Kälte und gutem Verschluss;
- kräftigere oder ausgebaute Weissweine: 2 bis 3 Tage, wenn kalt gelagert und geprüft;
- Roséweine: 1 bis 2 Tage, besonders wenn frische Frucht wichtig ist;
- leichte Rotweine: 1 bis 2 Tage, besser leicht kühl;
- strukturierte Rotweine: 2 bis 4 Tage mit guter Preservation;
- Schaumweine: derselbe Service oder 24 Stunden mit zuverlässigem Druckverschluss;
- fortifizierte Weine: oft länger, aber nicht ohne Kontrolle.

Diese Angaben sind keine automatische Ausschankfreigabe. Sie sind Prüfzeitpunkte. Wenn eine Flasche ihr Fenster erreicht, muss sie probiert werden.

## Sauerstoff, Temperatur und Füllstand

Drei Variablen erklären die meisten Probleme. Erstens Sauerstoff: Je mehr Luft in der Flasche bleibt, desto schneller verändert sich der Wein. Eine Flasche mit einem Drittel Wein entwickelt sich schneller als eine fast volle Flasche. Zweitens Temperatur: Wärme beschleunigt Alterung. Auch viele offene Rotweine halten besser im Kühlschrank und werden vor dem Servieren temperiert. Drittens Handling: offen stehen lassen, häufig bewegen oder Licht und Wärme aussetzen verkürzt die Nutzungsdauer.

Die Serviceregel kann einfach sein: immer verschliessen, immer datieren, wenn sinnvoll kalt stellen und vor dem erneuten Verkauf probieren. Wenn niemand weiss, wann eine Flasche geöffnet wurde, ist sie bereits ein Risiko.

## Preservation-Systeme: wann sie sinnvoll sind

Nicht jedes Restaurant braucht dasselbe System. Ein einfacher Stopfen hilft wenig, aber mehr als nichts. Eine Vakuumpumpe kann bei schneller Rotation helfen. Inertgas reduziert Sauerstoffkontakt. Nadelbasierte oder fortgeschrittene Systeme erlauben Premiumgläser, ohne die Flasche vollständig zu öffnen.

Die Entscheidung sollte wirtschaftlich und operativ sein. Wenn ein Einstiegswein im selben Service rotiert, reichen Kälte und Verschluss oft. Wenn ein Premiumglas langsam verkauft wird, macht Öffnen ohne Preservation aus theoretischer Marge echten Verlust. Vor dem Kauf eines Systems sollten Daten betrachtet werden: Anzahl Glasweine, Durchschnittspreis, tatsächlich ausgeschenkte Gläser pro Flasche, Tage offen und entsorgter Wein.

## Tagesprotokoll für Glaswein

Ein nützliches Protokoll hat fünf Schritte:

1. Datum und Uhrzeit der Öffnung notieren.
2. Schicht oder verantwortliche Person markieren.
3. Nach Stil lagern: kalt, Stopfen, Gas oder System.
4. Vor dem ersten Service am Folgetag probieren.
5. Aktion entscheiden: verkaufen, schulen, zurückziehen, Küche oder entsorgen.

Der vierte Schritt ist entscheidend. Nicht der Gast soll entdecken, dass ein Glas müde ist. Eine kleine Probe zu Servicebeginn schützt den Tisch und schult das Team.

## Ist die Flasche noch verkaufbar?

Stellen Sie drei Fragen:

- Ist das Aroma noch sauber, erkennbar und angenehm?
- Hat der Gaumen noch Frische, Balance und Nachhall?
- Ist das Glas den Preis auf der Karte noch wert?

Wenn die dritte Antwort unsicher ist, sollte der Wein nicht als normales Glas verkauft werden. Er kann für interne Schulung, Küche oder den Vergleich von Oxidation genutzt werden. Der Verlust gehört aber nicht zum Gast.

## Häufige Fehler

Der erste Fehler ist zu viele Glasreferenzen zu öffnen, ohne Rotation zu schätzen. Der zweite ist zu glauben, Kälte repariere jeden Wein. Der dritte ist empfindliche und robuste Stile gleich zu behandeln. Der vierte ist Öffnungen nicht zu dokumentieren. Der fünfte ist Premiumgläser ohne notwendiges Preservation-System auf der Karte zu halten.

Es gibt auch einen kulturellen Fehler: Angst, eine Flasche zurückzuziehen. Rechtzeitig zurückziehen heisst nicht nur Geld verlieren. Es kann Beschwerden vermeiden, Reputation schützen und zeigen, welche Referenzen nicht als Glaswein funktionieren.

