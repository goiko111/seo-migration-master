Eine offene Weinflasche richtig aufzubewahren ist kein Nebenthema. In Restaurants entsteht Verlust oft durch kleine wiederholte Entscheidungen: eine Flasche wird "vorsichtshalber" geöffnet, ein Weisswein bleibt warm stehen, ein Rotwein wird drei Tage nach dem Öffnen ausgeschenkt, ein Schaumwein verliert Druck oder ein Premiumglas wird ohne Preservation-System angeboten. Jede Situation kostet Marge und Vertrauen.

Wein im Service zu lernen heisst auch zu wissen, wann ein Wein noch lebendig ist und wann er nicht mehr serviert werden sollte. Es gibt keine einzige Regel. Aromatische Weissweine, leichte Rotweine, strukturierte Rotweine, fortifizierte Weine und Schaumweine verändern sich nach dem Öffnen unterschiedlich. Das Team braucht ein einfaches Protokoll: Öffnung notieren, Sauerstoffkontakt begrenzen, Temperatur kontrollieren, vor dem Service probieren und entscheiden, ob die Flasche verkauft, für Schulung genutzt, in die Küche gegeben oder entsorgt wird.

