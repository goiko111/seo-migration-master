## Perguntas frequentes

**Todas as garrafas abertas devem ir ao frio?**  
Nem todas, mas muitos vinhos abertos conservam-se melhor frios, incluindo tintos leves e médios. Depois voltam à temperatura de serviço.

**Vácuo é igual a gás inerte ou Coravin?**  
Não. Pode ajudar em rotação rápida, mas não substitui preservação forte para copos caros ou lentos.

**Quando retirar um vinho a copo da carta?**  
Quando não vende rápido o suficiente para acabar a garrafa na janela útil ou quando exige preservação que o restaurante não tem.

