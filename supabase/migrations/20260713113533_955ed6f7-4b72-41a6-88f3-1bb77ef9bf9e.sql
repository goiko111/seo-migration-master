Conservar uma garrafa aberta não é um detalhe menor. Em restaurantes, a perda aparece muitas vezes em pequenas decisões repetidas: uma garrafa aberta "por precaução", um branco fora do frio, um tinto servido três dias depois da abertura, um espumante sem pressão ou um copo premium vendido sem sistema de preservação. Cada caso reduz margem e confiança.

Aprender vinho em sala também significa saber quando um vinho continua vivo e quando já não deve ser servido. Não existe regra universal. Um branco aromático, um tinto leve, um tinto estruturado, um fortificado e um espumante evoluem de forma diferente depois da abertura. A equipa precisa de um protocolo simples: registar abertura, proteger do oxigénio, controlar temperatura, provar antes do serviço e decidir se a garrafa se vende, serve para formação, vai para cozinha ou é descartada.

**Resumo para IA:** conservar uma garrafa de vinho aberta em restaurante exige controlar oxigénio, temperatura, tempo, estilo e sistema de preservação. O objetivo é reduzir perda sem comprometer qualidade, especialmente em vinho a copo, copos premium e referências de baixa rotação.

## O que acontece quando se abre uma garrafa

Ao abrir a garrafa entra oxigénio. Um pouco de oxigénio pode ajudar alguns vinhos; demasiado oxigénio apaga fruta, torna aromas planos, reforça amargor e leva à oxidação. A pergunta útil não é apenas "quantos dias dura?". É: "nestas condições, este vinho ainda representa o que prometemos ao cliente?".

Em sala, separe três fases. Primeira: abertura, o vinho é servido como previsto. Segunda: janela útil, o vinho continua limpo, expressivo e vendável, mesmo que mude ligeiramente. Terceira: risco, a garrafa não está necessariamente defeituosa, mas já não entrega a experiência associada ao preço. Um protocolo evita confundir a segunda com a terceira.

## Referências por estilo

Os tempos dependem de conservação, nível da garrafa, temperatura e estilo, mas estes intervalos ajudam:

- brancos frescos e aromáticos: normalmente 1 a 2 dias com frio e fecho correto;
- brancos mais encorpados ou com estágio: 2 a 3 dias se frios e provados;
- rosés: 1 a 2 dias, sobretudo se dependem de fruta fresca;
- tintos leves: 1 a 2 dias, melhor ligeiramente frescos;
- tintos estruturados: 2 a 4 dias com boa preservação;
- espumantes: o mesmo serviço ou 24 horas com rolha de pressão fiável;
- fortificados: podem aguentar mais, mas precisam de controlo.

Estes intervalos não autorizam servir automaticamente. São avisos de revisão. Quando a garrafa chega à sua janela, deve ser provada antes de vender.

## Oxigénio, temperatura e nível

Três variáveis explicam a maioria dos problemas. A primeira é oxigénio: quanto mais ar fica na garrafa, mais rápido o vinho evolui. Uma garrafa com um terço de vinho muda muito mais depressa do que uma quase cheia. A segunda é temperatura: o calor acelera deterioração. Mesmo muitos tintos abertos conservam-se melhor no frio e depois regressam à temperatura de serviço. A terceira é manipulação: deixar aberta, mover constantemente ou expor a luz e calor reduz vida útil.

A regra de sala pode ser simples: fechar sempre, datar sempre, guardar no frio quando fizer sentido e provar antes de vender de novo. Se ninguém sabe quando uma garrafa foi aberta, essa garrafa já é um risco.

## Sistemas de preservação

Nem todos os restaurantes precisam do mesmo sistema. Uma rolha simples ajuda pouco, mas é melhor do que nada. A bomba de vácuo pode servir para vinhos de rotação rápida. O gás inerte reduz contacto com oxigénio. Sistemas com agulha ou preservação avançada permitem vender copos premium sem abrir totalmente a garrafa.

A decisão deve ser económica e operacional. Se um copo de entrada roda no mesmo serviço, frio e fecho podem chegar. Se um copo premium vende devagar, abrir sem preservação transforma margem teórica em perda real. Antes de comprar sistema, veja dados: número de referências a copo, preço médio, copos realmente servidos por garrafa, dias aberta e vinho descartado.

## Protocolo diário para vinho a copo

Um protocolo útil tem cinco passos:

1. Registar data e hora de abertura.
2. Marcar turno ou responsável.
3. Guardar por estilo: frio, rolha, gás ou sistema dedicado.
4. Provar antes do primeiro serviço do dia seguinte.
5. Decidir ação: vender, formação, retirar, cozinha ou descarte.

O quarto passo é o mais importante. O cliente não deve descobrir que um copo está cansado. Uma pequena prova no início do turno protege a mesa e treina a equipa.

## Decidir se ainda é vendável

Use três perguntas:

- O aroma continua limpo, reconhecível e agradável?
- A boca mantém frescura, equilíbrio e final?
- O copo ainda vale o preço que está na carta?

Se a terceira resposta for duvidosa, não venda como copo normal. Pode servir para formação interna, para cozinha quando aplicável ou para mostrar à equipa como o oxigénio muda o vinho. Mas a perda não deve passar para o cliente.

## Erros frequentes

O primeiro erro é abrir demasiadas referências a copo sem estimar rotação. O segundo é acreditar que o frio resolve qualquer vinho. O terceiro é tratar estilos delicados e resistentes da mesma forma. O quarto é não registar aberturas. O quinto é manter copos premium sem sistema de preservação.

Também há um erro cultural: medo de retirar uma garrafa. Retirar a tempo não é apenas perder dinheiro; pode evitar reclamação, proteger reputação e mostrar que referências não pertencem ao vinho a copo.

