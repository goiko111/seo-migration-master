export function getLocalizedResources(
  lang: string
): ResourcePageData[] {
  // Normalize language code
  const normalizedLang = lang.toLowerCase().split('-')[0];

  // Valid languages: es (Spanish/default), en, it, fr, de, pt
  const supportedLangs = ['en', 'it', 'fr', 'de', 'pt'];
  const targetLang = supportedLangs.includes(normalizedLang) ? normalizedLang : 'es';

  return newResources.map((resource) => {
    if (targetLang === 'es') {
      // Return original Spanish version unchanged
      return resource;
    }

    const translation = translations[resource.slug]?.[targetLang];
    if (!translation) {
      // Fallback to Spanish if no translation found
      return resource;
    }

    // Apply translations while keeping structural elements (slugs, icons, links, downloads) unchanged
    return {
      ...resource,
      metaTitle: translation.metaTitle,
      metaDescription: translation.metaDescription,
      badgeLabel: translation.badgeLabel,
      heroTitle: translation.heroTitle,
      heroDescription: translation.heroDescription,
      heroStats: resource.heroStats.map((stat, i) => ({
        ...stat,
        label: translation.heroStatsLabels[i] || stat.label,
      })),
      problemTitle: translation.problemTitle,
      problemDescription: translation.problemDescription,
      problemPoints: resource.problemPoints.map((point, i) => ({
        ...point,
        text: translation.problemPointsText[i] || point.text,
      })),
      contentTitle: translation.contentTitle,
      contentDescription: translation.contentDescription,
      sections: resource.sections.map((section, sectionIdx) => ({
        ...section,
        title: translation.sectionTitles[sectionIdx] || section.title,
        points: section.points.map(
          (point, pointIdx) =>
            translation.sectionPoints[sectionIdx]?.[pointIdx] || point
        ),
      })),
      audienceTitle: translation.audienceTitle,
      audienceItems: translation.audienceItems,
      whenToUseTitle: translation.whenToUseTitle,
      whenToUseItems: translation.whenToUseItems,
      benefits: resource.benefits.map((benefit, i) => ({
        ...benefit,
        title: translation.benefitTitles[i] || benefit.title,
        desc: translation.benefitDescs[i] || benefit.desc,
      })),
      faqs: resource.faqs.map((faq, i) => ({
        q: translation.faqQuestions[i] || faq.q,
        a: translation.faqAnswers[i] || faq.a,
      })),
      ctaFinalTitle: translation.ctaFinalTitle,
      ctaFinalDescription: translation.ctaFinalDescription,
    } as ResourcePageData;
  });
}

/**
 * Get a single resource by slug in a specific language
 * Usage: getLocalizedResource('plantilla-estrategia-vinos-por-copa', 'en')
 */
export function getLocalizedResource(
  slug: string,
  lang: string
): ResourcePageData | undefined {
  const resources = getLocalizedResources(lang);
  return resources.find((r) => r.slug === slug);
}
